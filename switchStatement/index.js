// 1-QUESTION

var place = 'fourth'

switch(place){

case 'first':
    console.log("GOLD");
    break;

case 'second':
    console.log("SILVER");
    break;

case 'third':
    console.log("BRONZE");
    break;

default:
    console.log("NO MEDAL");    
}


// 2- QUESTION

var light = 'green';

switch(light) {
    case 'green':
        console.log("Drive");
        break;
    case 'orange':
        console.log("Get ready");
        break;
    case 'red':
        console.log("Don't drive");
        break;
    default:
        console.log('The light is not green, orange, or red');
        break;
 }


